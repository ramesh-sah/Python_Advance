from math import * 

def calculate_factorial(x):
    return factorial(x)

def calculate_fabs(x):
    return fabs(x)

def calculate_ceil(x):
    return ceil(x)
def calculate_floor(x):
    return floor(x)

def calculate_square_root(x):
    return sqrt(x)

if __name__ == '__main__':
    print(calculate_factorial(5))
    print(calculate_fabs(-10.5))
    print(calculate_ceil(3.7))
    print(calculate_floor(10.3))
    print(calculate_square_root(9))

    


